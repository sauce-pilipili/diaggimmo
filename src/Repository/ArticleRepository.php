    public function findAllWithJoin()
    {
        return $this->createQueryBuilder('a')
            ->select('a','i')
            ->join('a.image','i')
            ->orderBy('a.createdDate', 'DESC')
            ->setMaxResults(3)
            ->getQuery()
            ->getResult()
        ;
    }


